package Day05;

public class Day05_1_1 {
	public static void main(String[] args) {

		for (int i = 1; i <= 9; i++) {
			int cal = 2 * i;
			System.out.println("2 * " + i + " = " + cal);
		}

		System.out.println("==========================");

		for (int i = 1; i <= 9; i++) {
			int cal = 15 * i;
			System.out.println("15 * " + i + " = " + cal);
		}
	}
}
